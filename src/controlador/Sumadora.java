
package controlador;

/**
 *
 * @author alanm
 * Clase que suma numeros
 */
public class Sumadora {
    /**
     * Costructor vacio
     */
    public Sumadora() {
    }
    /**
     * Suma double a y double b y regresa la suma
     * @param a El primer numero a sumar
     * @param b El segundo numero a sumar
     * @return La suma de dos valores double
     */
    public double sumar(double a, double b) {
        double suma=a+b;
        return suma;
    }
}
